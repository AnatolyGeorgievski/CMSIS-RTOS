uint8_t Rev8(uint8_t a){
	const uint8_t m4=0x0F, m2=0x33, m1=0x55;
	a = (a &~m4)>>4 | (a & m4)<<4;
	a = (a &~m2)>>2 | (a & m2)<<2;
	a = (a &~m1)>>1 | (a & m1)<<1;
	return a;
}
